"""
Рекламные тексты для Яндекс Директ.

Лимиты Яндекс Директ:
  Заголовок 1 — 56 символов
  Заголовок 2 — 30 символов
  Текст       — 81 символ
  Отображаемый путь — 20 символов каждый
  Быстрые ссылки — 30 символов заголовок, 60 символов описание
"""

from dataclasses import dataclass, field


@dataclass
class AdVariant:
    title1: str       # ≤56 символов
    title2: str       # ≤30 символов
    text: str         # ≤81 символ
    display_path: str  # путь в отображаемом URL (≤20 символов)


@dataclass
class QuickLink:
    title: str        # ≤30 символов
    url_suffix: str   # часть URL (/?ql=1 и т.д.)
    description: str  # ≤60 символов — показывается в премиум-размещении


@dataclass
class AdGroup:
    name: str
    category: str              # совпадает с classifier.CATEGORY_MAP
    intent_filter: list[str]   # какие уровни интента брать ("high", "medium")
    ads: list[AdVariant]
    quick_links: list[QuickLink]
    group_negatives: list[str]  # минус-слова уровня группы


