"""
Классификатор интента ключевых слов.

Три уровня:
  high   — человек активно ищет специалиста прямо сейчас
  medium — человек осознаёт проблему и ищет информацию / решение
  low    — общий интерес к теме, без явной боли
"""

import re
from dataclasses import dataclass, field

from scraper import RawKeyword


@dataclass
class ClassifiedKeyword:
    keyword: str
    frequency: int
    source_seed: str
    intent: str                         # "high" | "medium" | "low"
    emotion: list[str] = field(default_factory=list)
    categories: list[str] = field(default_factory=list)  # может быть несколько
    ad_headline: str = ""               # готовый заголовок для Директа


# ---------------------------------------------------------------------------
# Паттерны интента
# ---------------------------------------------------------------------------

HIGH_INTENT_PATTERNS = [
    r"психолог.*(запись|онлайн|цена|стоимость|отзыв|найти|обратиться)",
    r"(найти|нужен|нужна|хороший|хорошая|лучший|лучшая).*(психолог|терапевт)",
    r"(консультация|приём|сессия|услуги).*(психолог|терапевт|специалист)",
    r"семейн\w+ (психолог|терапевт)",
    r"(куда|к кому).*(обратиться|идти|записаться).*(брак|муж|жена|пара|отношени)",
    r"помощь (психолога|специалиста).*(брак|пара|семья|отношени)",
    r"работа с (психологом|терапевтом)",
    r"психотерапевт.*(пар|брак|семь|отношени)",
    r"(запись|записаться).*(психолог|терапевт)",
    r"(онлайн|дистанционн).*(психолог|терапевт).*(пар|семь|брак)",
]

MEDIUM_INTENT_PATTERNS = [
    r"(что делать|как быть|как справиться|как пережить|как восстановить)",
    r"(муж|жена|партнёр).*(изменил|изменила|изменяет|ушёл|ушла|охладел|охладела)",
    r"(потеря|нет|потерял|потеряла).*(близости|доверия|любви|интереса|чувств)",
    r"кризис.*(брак|семья|отношени)",
    r"(развод|расставани).*(не хочу|боюсь|избежать|как избежать)",
    r"(конфликт|ссор|скандал).*(постоянн|каждый день|не могу|надоело)",
    r"(не понимает|не слышит|отдалил|стал чужим).*(муж|жена|партнёр)",
    r"как (вернуть|наладить|улучшить|восстановить).*(отношени|брак|близость|доверие)",
    r"(предательство|измена).*(пережить|простить|забыть|как)",
    r"(сохранить|спасти).*(брак|семью|отношени)",
    r"(разлад|холод|отчуждени).*(семь|брак|отношени)",
    r"не (хочу|могу) (разводиться|расставаться)",
]

EMOTION_MAP = {
    "отчаяние":      [r"не знаю что делать", r"всё рухнуло", r"не могу так больше", r"безвыходн", r"тупик"],
    "надежда":       [r"можно ли (вернуть|исправить|наладить)", r"есть ли шанс", r"хочу сохранить", r"хочу вернуть"],
    "гнев":          [r"измен\w+", r"предательств", r"ненавиж", r"невыносим", r"не могу простить"],
    "растерянность": [r"не понимаю", r"что происходит", r"почему (он|она|муж|жена)", r"как так"],
    "страх":         [r"боюсь (развод|потерять|остаться)", r"страшно", r"тревог"],
}

CATEGORY_MAP = {
    "специалист": [r"психолог", r"терапевт", r"консультац", r"сессия", r"запись", r"психотерапевт"],
    "измена":     [r"измен", r"предательств"],
    "развод":     [r"развод", r"расставани", r"уход из семьи"],
    "близость":   [r"близость", r"интимн", r"охладел", r"нет чувств", r"нет близости"],
    "конфликты":  [r"конфликт", r"ссор", r"скандал", r"не понимает", r"не слышит"],
    "кризис":     [r"кризис", r"тупик", r"разлад", r"отчуждени"],
    "доверие":    [r"доверие", r"ложь", r"обман", r"не верю"],
}

# Шаблоны заголовков для Директа по категории
AD_HEADLINE_TEMPLATES = {
    "специалист": "Семейный психолог онлайн | Запись сегодня",
    "измена":     "Помощь психолога после измены | Онлайн",
    "развод":     "Не хотите разводиться? Помогу сохранить брак",
    "близость":   "Вернуть близость в браке | Психолог для пар",
    "конфликты":  "Постоянные конфликты в паре? Решение есть",
    "кризис":     "Кризис в браке | Онлайн-консультация психолога",
    "доверие":    "Восстановить доверие в браке | Психолог",
    "другое":     "Психолог для пар | Консультация онлайн",
}


# ---------------------------------------------------------------------------
# Классификатор
# ---------------------------------------------------------------------------

def classify(raw: RawKeyword) -> ClassifiedKeyword:
    kw = raw.keyword.lower()
    return ClassifiedKeyword(
        keyword=raw.keyword,
        frequency=raw.frequency,
        source_seed=raw.source_seed,
        intent=_detect_intent(kw),
        emotion=_detect_emotions(kw),
        categories=_detect_categories(kw),
        ad_headline=_suggest_headline(kw),
    )


def _detect_intent(kw: str) -> str:
    for pattern in HIGH_INTENT_PATTERNS:
        if re.search(pattern, kw):
            return "high"
    for pattern in MEDIUM_INTENT_PATTERNS:
        if re.search(pattern, kw):
            return "medium"
    return "low"


def _detect_emotions(kw: str) -> list[str]:
    return [
        emotion
        for emotion, patterns in EMOTION_MAP.items()
        if any(re.search(p, kw) for p in patterns)
    ]


def _detect_categories(kw: str) -> list[str]:
    found = [
        cat
        for cat, patterns in CATEGORY_MAP.items()
        if any(re.search(p, kw) for p in patterns)
    ]
    return found or ["другое"]


def _suggest_headline(kw: str) -> str:
    cats = _detect_categories(kw)
    primary = cats[0] if cats else "другое"
    return AD_HEADLINE_TEMPLATES.get(primary, AD_HEADLINE_TEMPLATES["другое"])


def classify_all(raw_list: list[RawKeyword]) -> list[ClassifiedKeyword]:
    return [classify(r) for r in raw_list]
